<h2><?= $title?></h2>
<p>Ez az oldal járvűveket(autók, motorokat, vonatokat, kaminokat) tart nyílván.</p>
<br>
<p>6 db adatbázis gondoskodik az oldal megfelelő működéséről.</p>
<p>Regisztrálás után mindenki feltölthet egy általa kedvelt járművet a megfelelő kategóriába.</p>
<p>Fontos, hogy csak megfelelő tartalmat tölts fel. Hiszen az oldalon adminok találhatóak, akik minden posztot átnéznek
és ellenőrzik azt. Ha nem megfelől tartalmat töltesz fel tiltólistára kerülhetsz.</p>

<p>Az alábbi funkciókkal találkozhatsz:</p>
<ul>
    <li>Minden egyes funckciót csak bejelentlezve lehet használni.</li>
    <li>Mindenki csak a saját posztját szerkesztheti, a máséhoz nem fér hozzá</li>
    <li>Kivétel ezalól az admin. Az admin minden egyes  posztot módosíthat törölhet</li>
    <li>Lehet kép nélkül posztolni, ilyenkor egy előre beállított fénykép jelenik meg.</li>
    <li>Bejelentkezés nélkül minden jármű kategória exportálható.</li>
    <li>Bejelentkezés után mindenki több autót is feltölthet egyszerre egy csv fájllal. Ilyenkor
    az alap fénykép jelenik meg.</li>
</ul>

